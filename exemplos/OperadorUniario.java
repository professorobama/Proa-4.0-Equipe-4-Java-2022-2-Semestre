package exemplo;

public class OperadorUniario {

	public static void main(String[] args) {
	
		int numero = 10;
		System.out.println(++numero);
		System.out.println(++numero);
		System.out.println(numero++);
		System.out.println(numero);
		System.out.println(numero--);
		System.out.println(numero--);
		System.out.println(numero);
		System.out.println(--numero);
		System.out.println(numero);
	
	}
}
